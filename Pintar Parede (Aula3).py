altura = (int(input("Qual é a altura da parede?")))

largura = (int(input("Qual é a largura da parede?")))

area = (largura*altura)

print(f'A área da parede é {area} metros quadrados.')

litros = ((area*1)/2)

print(f'Os litros de tinta necessários para cobrir a parede são {litros} litros de tinta.')